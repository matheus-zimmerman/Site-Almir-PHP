A história de Fate/Stay Night gira em torno de Shirou Emiya, um jovem que entra contra a própria vontade em um torneio conhecido como a Guerra do Santo Graal. No evento, magos poderosos competem para tentar ganhar o Santo Graal, um objeto que supostamente tem o poder de atender a qualquer desejo.

